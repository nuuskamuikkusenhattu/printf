#include "libft.h"

int	ft_num_length(int n)
{
	int		i;

	i = 1;
	if (n < 0)
	{
		i++;
		n = n * -1;
	}
	while (n > 10)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
